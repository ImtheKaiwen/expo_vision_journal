const dictionaries = { tr, en };

const I18nContext = createContext();

export function I18nProvider({ children }) {
  const [langCode, setLangCode] = useState('tr');

  useEffect(() => {
    let mounted = true;
    getAppSettings().then((s) => {
      if (mounted && s.language) {
        setLangCode(s.language);
      }
    });
    return () => (mounted = false);
  }, []);

  useEffect(() => {
    const sub = DeviceEventEmitter.addListener(APP_SETTINGS_CHANGED, async () => {
      const s = await getAppSettings();
      if (s.language) setLangCode(s.language);
    });
    return () => sub.remove();
  }, []);

  const t = (key) => {
    const dict = dictionaries[langCode] || dictionaries['tr'];
    return dict[key] || key;
  };

  const changeLanguage = async (code) => {
    setLangCode(code);
    await saveAppSettings({ language: code });
    DeviceEventEmitter.emit(APP_SETTINGS_CHANGED);
  };

  return (
    <I18nContext.Provider value={{ t, langCode, changeLanguage }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}

/** 
 * Helper for accessing translations outside of components (e.g. App.js top level)
 * This doesn't react to language changes, it just gets the current state.
 */
export const getI18n = () => {
  // We can't easily get the saved language synchronously here without async storage,
  // so we'll just return a function that uses a best-effort or defaults to TR.
  // In a real app, you might sync this state globally.
  const t = (key) => {
    // Basic implementation: check TR then EN
    return tr[key] || en[key] || key;
  };
  return { t };
};
